package review4;

public class ContinuekeyWord {

    public static void main(String[] args) {

        // continue - skips current iteration, inside if condition, tell java to move to next iteration

        for (int i = 1; i <=5 ; i++) {

            if(i==3){
                continue;
            }

            System.out.println(i);
        }








    }
}
